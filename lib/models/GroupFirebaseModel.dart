class GroupFirebaseModel{
  String groupId;
  String groupName;
  String groupDesc;
  String contentTime;
  String photoGroup;

  String get getGroupId => groupId;
  set setGroupId(String value) => groupId = value;

  String get getGroupName => groupName;
  set setGroupName(String value) => groupName = value;

  String get getGroupDesc=> groupDesc;
  set setGroupDesc(String value) => groupDesc = value;

  String get getContentTime => contentTime;
  set setContentTime(String value) => contentTime = value;

  String get getPhotoGroup => photoGroup;
  set setPhotoGroup(String value) => photoGroup = value;

}